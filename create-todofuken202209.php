    function func_update($_post_type, $_post_slug, $tmp_post_param) {
        if (!exist_duplicates($_post_slug,$_post_type,(isset($tmp_post_param['post_parent'])? $tmp_post_param['post_parent']: 0))) {
            $tmp_id = wp_insert_post($tmp_post_param);
            usleep(100);
        }
        if ($tmp_id) {
            $tmp_post_param['ID'] = $tmp_id;
            $tmp_post_param['post_status'] = 'publish';
            wp_update_post($tmp_post_param);
            usleep(100);
            return $tmp_id;
        }
        return false;
    };
    // 全く同じslugが登録されていないか判定
    function exist_duplicates($slug,$posttype="any",$parent=0){
        $args = array(
            'post_status' => 'publish',
            'name' => $slug,
            'post_type' => $posttype,
            'post_parent' => $parent,
        );
        // $mposts = get_posts($args);
        $myquerytmp = new WP_Query();
        $myquerytmp->query($args);
        $mycount = $myquerytmp->found_posts; // 非公開の記事も含まれるので注意
        return $mycount;
    }
